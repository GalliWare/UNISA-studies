// Basic program to output a poem
// from Alice’s Adventures in Wonderland by Lewis Carroll to the console
#include <iostream>
using namespace std;
int main()
{
  cout << "Twinkle, twinkle, little bat!" << endl;
  cout << "How I wonder what you’re at?" << endl;
  cout << "Up above the world you fly," << endl;
  cout << "Like a tea-tray in the sky." << endl;
  return 0;
}